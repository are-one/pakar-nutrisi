                    <div class="tab-pane active" id="tab-1">
                        <h3>Faringitis</h3>
                        <img src="img/penyakit/faringitis.jpg" alt="" class="img-fluid">
                        <p>
                            Faringitis merupakan peradangan pada faring sehingga timbul rasa nyeri pada waktu menelan makanan ataupun kerongkongan terasa kering. Gangguan ini disebabkan oleh infeksi bakteri atau virus dan dapat juga disebabkan terlalu banyak merokok (Aryulina et al., 2004).
                        </p>
                    </div>
                    <div class="tab-pane" id="tab-2">
                        <h3>Polip</h3>
                        <img src="img/penyakit/polip.jpg" alt="" class="img-fluid">
                        <p>
                            Polip merupakan penyempitan saluran pernapasan akibat terjadinya pembengkakan kelenjar limfe. Penderita polip akan menjadi sesak napas ketika mengalaminya (Dhiyaulhaq, 2015).
                        </p>
                    </div>
                    <div class="tab-pane" id="tab-3">
                        <h3>Otitis Eksterna (OE)</h3>
                        <img src="img/penyakit/otitiseksterna.jpg" alt="" class="img-fluid">
                        <p>
                            Otitis eksterna merupakan suatu peradangan pada liang telinga luar baik akut maupun kronis, yang sering kali dihubungkan dengan infeksi oleh bakteri, jamur, dan virus yang menyertai maserasi kulit dan jaringan subkutan. Penyakit otitis eksterna bisa terjadi pada semua umur dan kejadiannya cukup sering terjadi di masyarakat luas (Medika & No, 2018).
                        </p>
                    </div>
                    <div class="tab-pane" id="tab-4">
                        <h3>Noise-Induced Hearing Loss (NIHL)/gangguan pendengaran akibat bising</h3>
                        <img src="img/penyakit/nihl.png" alt="" class="img-fluid">
                        <p>
                            Noise-induced hearing loss adalah bentuk kehilangan pendengaran akibat paparan suara bising dalam intensitas yang berbahaya secara kronik. NIHL merupakan bentuk penurunan pendengaran tersering kedua setelah kondisi penurunan pendengaran terkait usia. Kondisi ini menyebabkan morbiditas yang tinggi karena penderita dapat mengalami kesulitan dalam komunikasi sehari-hari (Anonim, 2017).
                        </p>
                    </div>
                    <div class="tab-pane" id="tab-5">
                        <h3>Vestibulitis</h3>
                        <img src="img/penyakit/vestibilitis.jpg" alt="" class="img-fluid">
                        <p>
                            Vestibulitis merupakan proses peradangan atau inflamasi pada lapisan kulit yang terletak di bagian depan lubang hidung. Vestibulitis kebanyakan disebabkan oleh infeksi bakteri, walaupun kadang-kadang bisa juga oleh virus, ditandai dengan pembengkakan, nyeri pada hidung dan hidung terasa tersumbat (Anonim, 2017).
                        </p>
                    </div>
                    <div class="tab-pane" id="tab-6">
                        <h3>Meniere</h3>
                        <img src="img/penyakit/meniere.jpg" alt="" class="img-fluid">
                        <p>
                            Meniere adalah gangguan telinga bagian dalam yang juga dikenal sebagai hidrops endolimfatik idiopatik. Hidrops endolimfatik mengacu pada suatu kondisi peningkatan tekanan hidrolik dalam sistem endolimfatik telinga dalam. Penyakit Meniere adalah suatu gangguan kronis telinga dalam, tidak fatal tetapi mengganggu kualitas hidup (Sutarni et al., 2018).
                        </p>
                    </div>
                    <div class="tab-pane" id="tab-7">
                        <h3>Rhinosinusitis</h3>
                        <img src="img/penyakit/rhinosinusitis.png" alt="" class="img-fluid">
                        <p>
                            Rhinosinusitis didefinisikan sebagai kondisi inflamasi yang melibatkan sinus maxillaris, frontalis, sphenoidalis, dan ethmoidalis, yaitu empat struktur sinus yang mengelilingi cavum nasal. Rhinosinusitis timbul bila terjadi peradangan pada mukosa sinus paranasalis, biasanya disebabkan oleh alergi atau infeksi virus. Peradangan menyebabkan edema mukosa, produksi mucus yang terlalu banyak, dan pertumbuhan bakteri yang berlebihan. Rhinosinusitis dibagi menjadi tiga yaitu rhinosinusitis akut, subakut atau kronis (Saputra et al., 2017).
                        </p>
                    </div>
                    <div class="tab-pane" id="tab-8">
                        <h3>Stomatitis</h3>
                        <img src="img/penyakit/stomatitis.jpg" alt="" class="img-fluid">
                        <p>
                            Stomatitis adalah peradangan jaringan mukosa mulut yang disebabkan oleh beberapa faktor (Kunsputri & Suhartiningtyas, n.d.). Faktor-faktor tersebut antara lain alergi, trauma, stres, bad habit, autoimmune, sistem endokrin, psikologi, herediter, dan defisiensi nutrisi. Stomatitis sendiri berarti inflamasi pada mulut (Leman, 2015).
                        </p>
                    </div>
                    <div class="tab-pane" id="tab-9">
                        <h3>Sinusitis</h3>
                        <img src="img/penyakit/sinusitis.png" alt="" class="img-fluid">
                        <p>
                            Sinusitis adalah peradangan pada sinus akibat alergi, infeksi virus, bakteri atau jamur. Sinusitis bisa terjadi pada salah satu dari keempat sinus yang ada (maksilaris, etmoidalis, frontalis, atau sfenoidalis). Sebagian besar penyebab sinusitis adalah infeksi virus, infeksi bakteri seperti Streptococcus pneumonia dan Haemophilus influenza, serta infeksi jamur (Aspergillus) (Indrawati & Razimin, 2013). Sinusitis merupakan peradangan pada mukosa sinus paranasal. Peradangan ini banyak dijumpai pada anak dan dewasa yang biasanya didahului oleh infeksi saluran napas atas (Ismayati, 2010).
                        </p>
                    </div>
                    <div class="tab-pane" id="tab-10">
                        <h3>Epiglotitis</h3>
                        <img src="img/penyakit/epiglotitis.jpg" alt="" class="img-fluid">
                        <p>
                            Epiglottitis adalah suatu infeksi pada epiglotis, yang biasanya menyebabkan penyumbatan saluran pernapasan dan kematian. Epiglotis adalah tulang rawan yang berfungsi sebagi katup pada pita suara (laring) dan tabung udara (trakea), yang akan menutup selama proses menelan berlangsung (Ismayati, 2010).
                        </p>
                    </div>
                    <div class="tab-pane" id="tab-11">
                        <h3>Otitis Media Akut</h3>
                        <img src="img/penyakit/otitis.jpg" alt="" class="img-fluid">
                        <p>
                            Otitis media akut adalah infeksi akut telinga tengah. Penyebab utamanya adalah masuknya bakteri patogenik kedalam telinga tengah yang normalnya steril ketika terdapat disfungsi tuba eustakhian, obstruksi yang disebabkan oleh infeksi saluran pernapasan atas, inflamasi struktur sekitarnya (sinusitis), atau oleh reaksi alergi (rinitis alergik) (Baughman dan Hackley, 1996).
                        </p>
